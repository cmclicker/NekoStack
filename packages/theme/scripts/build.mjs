// ---------------- 6. dist/tailwind.preset.cjs ----------------
function toVars(obj, p = 'neko') {
  const r = {};
  for (const [k, v] of Object.entries(obj)) {
    const np = `${p}-${k}`;
    if (v !== null && typeof v === 'object' && !Array.isArray(v)) {
      if ('value' in v) r[k] = `var(--${np})`;
      else r[k] = toVars(v, np);
    } else {
      r[k] = `var(--${np})`;
    }
  }
  return r;
}
// Tailwind preset maps to the default-theme/default-mode semantic vars (correct since they
// resolve to whatever theme is active via CSS variables at runtime).
const mergedForTw = { ...tokens.base, color: { ...tokens.base.color, semantic: tokens.themes[defaultTheme].modes[defaultMode].color.semantic } };
const mapped = toVars(mergedForTw, 'neko');
const preset = {
  theme: {
    extend: {
      colors: mapped.color || {},
      spacing: mapped.spacing || {},
      borderRadius: mapped.radius || {},
      fontFamily: mapped.font || {},
      fontSize: mapped['font-size'] || {},
      lineHeight: mapped['line-height'] || {},
      transitionDuration: mapped.motion?.duration || {},
      transitionTimingFunction: mapped.motion?.easing || {},
      zIndex: mapped['z-index'] || {},
      boxShadow: mapped.color?.shadow || {},
    },
  },
};
fs.writeFileSync(
  path.join(distDir, 'tailwind.preset.cjs'),
  `/**\n * @nekostack/theme — Tailwind preset. Generated by build.mjs.\n * @type {import('tailwindcss').Config}\n */\nmodule.exports = ${JSON.stringify(preset, null, 2)};\n`,
);

